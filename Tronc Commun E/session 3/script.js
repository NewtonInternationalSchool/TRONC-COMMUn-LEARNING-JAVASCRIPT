//Different types of Variables
//I) Primitive types of Variables
//1) String
let firstName = 'Ines';
firstName = 'Lina';
console.log(firstName);

//2) Numbers
let age = 16;
console.log(age);
console.log(typeof age);

//3) Boolean
let isLinaFasting = false;
console.log(isLinaFasting);
console.log(typeof isLinaFasting)

//4) Undefined
let money;
console.log(money);
console.log(typeof money);

//5) Null
let oldPeopleInThisClass = null;
console.log(oldPeopleInThisClass);
console.log(typeof oldPeopleInThisClass);


